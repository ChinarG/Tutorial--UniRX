public class ChinarRxComplete : MonoBehaviour
{
    /// <summary>
    /// 初始化函数
    /// </summary>
    void Start()
    {
        //协程完成
        //完成函数，无参，所以不用用 _
        Observable.FromCoroutine(A).Subscribe(_ => print("执行 Next！"), () => print("执行 Complete"));

        //事件完成
        //完成函数，无参，所以不用用 _
        Observable.Timer(TimeSpan.FromSeconds(3)).Subscribe(_ => print("3秒执行 Next！"), () => print("3秒执行 Complete"));
    }


    IEnumerator A()
    {
        yield return new WaitForSeconds(1);
        print("A Success!");
    }
}