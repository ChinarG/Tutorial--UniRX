public class ChinarRxRegister : MonoBehaviour
{
    private Button     backButton;
    private Button     registerButton;
    private InputField userInputField;
    private InputField passInputField;
    private InputField pass2InputField;


    void Awake()
    {
        backButton      = transform.Find("Back Button").GetComponent<Button>();
        registerButton  = transform.Find("Register1 Button").GetComponent<Button>();
        userInputField  = transform.Find("User1").GetComponent<InputField>();
        passInputField  = transform.Find("Pass1").GetComponent<InputField>();
        pass2InputField = transform.Find("Pass2").GetComponent<InputField>();
    }


    /// <summary>
    /// 初始化函数
    /// </summary>
    void Start()
    {
        //结束编辑时，提交
        userInputField.OnEndEditAsObservable().Subscribe(_ => passInputField.Select());          //按下回车，直接切换到密码输入框
        passInputField.OnEndEditAsObservable().Subscribe(_ => pass2InputField.Select());         //回车后，直接登录
        pass2InputField.OnEndEditAsObservable().Subscribe(_ => registerButton.onClick.Invoke()); //回车后，直接登录
        backButton.OnClickAsObservable().Subscribe(_ => print("返回"));
        registerButton.OnClickAsObservable().Subscribe(_ => print("注册"));
    }
}