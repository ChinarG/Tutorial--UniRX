public class ChinarAsyncOperation : MonoBehaviour
{
    /// <summary>
    /// 初始化函数
    /// </summary>
    void Start()
    {
        Resources.LoadAsync("Test-Canvas").AsAsyncOperationObservable()             //异步加载并转为 ：AsyncOperationObservable
            .Subscribe(resourceRequest => { Instantiate(resourceRequest.asset); }); //将返回的资源请求，实例化出来
        AsAsyncOperationObservable_LoadSceneAsync();
    }


    /// <summary>
    /// 异步加载场景
    /// </summary>
    private void AsAsyncOperationObservable_LoadSceneAsync()
    {
        ScheduledNotifier<float> progress = new ScheduledNotifier<float>();           //预定通知
        SceneManager.LoadSceneAsync("17-异步加载测试").AsAsyncOperationObservable(progress) //异步加载场景 返回异步操作对象
            .Subscribe(_ => print("场景加载完成！"));                                        //当异步操作完成，输出“场景加载完成！”
        progress.Subscribe(_ => print(_));                                            //进度值变更，输出进度值
    }
}