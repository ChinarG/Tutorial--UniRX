public class ChinarRxWWW : MonoBehaviour
{
    /// <summary>
    /// 初始化函数
    /// </summary>
    void Start()
    {
    }


    void Rx()
    {
        ObservableWWW.Get("http://chinar.xin").Subscribe(_ => print(_), e => print(e));
        //第一个 print是请求正常信息，第二个是打印错误信息
        ObservableWWW.Get("http://chinar.xin/asdf").Subscribe(print, print);

        //同样可合并操作
        var streamA = ObservableWWW.Get("http://chinar.xin");
        var streamB = ObservableWWW.Get("http://baidu.com");
        Observable.WhenAll(streamA, streamB).Subscribe(_ =>
        {
            print(_[0].Substring(1, 16));
            print(_[1].Substring(1, 16));
        });
    }
}