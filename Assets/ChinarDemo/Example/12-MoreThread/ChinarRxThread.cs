public class ChinarRxThread : MonoBehaviour
{
    /// <summary>
    /// 初始化函数
    /// </summary>
    void Start()
    {
        var stream1 = Observable.Start(() =>
        {
            Thread.Sleep(TimeSpan.FromSeconds(1));
            return 1;
        }); //线程1
        var stream2 = Observable.Start(() =>
        {
            Thread.Sleep(TimeSpan.FromSeconds(2));
            return 2;
        }); //线程2

        //当线程执行完毕，将其他线程中的值，返回到主线程中
        Observable.WhenAll(stream1, stream2).ObserveOnMainThread().Subscribe(_ =>
        {
            print(_[0]);
            print(_[1]);
        });
    }
}